CREATE DEFINER=`paksamysql`@`%` FUNCTION `fn_getUserResult`(in_user_id INT, in_user_role VARCHAR(8)) RETURNS decimal(15,1)
BEGIN

	DECLARE v_em_specification DECIMAL(15,1);
    DECLARE v_em_communcation DECIMAL(15,1);
    DECLARE v_em_promptness DECIMAL(15,1);
    DECLARE v_em_professionalism DECIMAL(15,1);
    DECLARE v_em_rework DECIMAL(15,1);
    
    DECLARE v_hire_quality DECIMAL(15,1);
    DECLARE v_hire_communcation DECIMAL(15,1);
    DECLARE v_hire_expertise DECIMAL(15,1);
    DECLARE v_hire_professionalism DECIMAL(15,1);
    DECLARE v_hire_rework DECIMAL(15,1);
    
    DECLARE v_result DECIMAL(15,1) DEFAULT 0.0;

	IF in_user_role = '1' THEN
		
		/* 고용주 평점 구하기 */
		SELECT IFNULL(ROUND(em_specification/result_cnt, 1), 0) AS em_specification,
			   IFNULL(ROUND(em_communcation/result_cnt, 1), 0) AS em_communcation,
			   IFNULL(ROUND(em_promptness/result_cnt, 1), 0) AS em_promptness,
			   IFNULL(ROUND(em_professionalism/result_cnt, 1), 0) AS em_professionalism,
			   IFNULL(ROUND(em_rework/result_cnt, 1), 0) AS em_rework
		  INTO v_em_specification, 
			   v_em_communcation, 
			   v_em_promptness, 
			   v_em_professionalism, 
			   v_em_rework
		  FROM (SELECT in_user_id AS user_id) t1
			   LEFT OUTER JOIN 
			   (
				SELECT user_id, 
					   (SELECT COUNT(project_id) FROM free.tbl_user_result WHERE user_id = a.user_id AND user_role = a.user_role) AS result_cnt,
					   SUM(em_specification) AS em_specification,
					   SUM(em_communcation) AS em_communcation,
					   SUM(em_promptness) AS em_promptness,
					   SUM(em_professionalism) AS em_professionalism,
					   SUM(em_rework) AS em_rework
				  FROM free.tbl_user_result a
				 WHERE user_id = in_user_id
				   AND user_role = in_user_role
				 GROUP BY user_id, user_role
			   ) t2 
			   ON t1.user_id = t2.user_id
		;
		
		SELECT ROUND((v_em_specification + v_em_communcation + v_em_promptness + v_em_professionalism + v_em_rework) / 5, 1) INTO v_result;
        
    ELSEIF in_user_role = '2' THEN
		
