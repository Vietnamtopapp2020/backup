			   IFNULL(ROUND(hire_expertise/result_cnt, 1), 0) AS hire_expertise,
			   IFNULL(ROUND(hire_professionalism/result_cnt, 1), 0) AS hire_professionalism,
			   IFNULL(ROUND(hire_rework/result_cnt, 1), 0) AS hire_rework
		  INTO v_hire_quality, 
			   v_hire_communcation, 
			   v_hire_expertise, 
			   v_hire_professionalism, 
			   v_hire_rework
		  FROM (SELECT in_user_id AS user_id) t1
			   LEFT OUTER JOIN 
			   (
				SELECT user_id, 
					   (SELECT COUNT(project_id) FROM free.tbl_user_result WHERE user_id = a.user_id AND user_role = a.user_role) AS result_cnt,
					   SUM(hire_quality) AS hire_quality,
